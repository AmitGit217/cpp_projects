#include <cmath>
#include <iostream>

using namespace std;

int main() {

  cout << "Absolute value of -5: " << abs(5) << endl;

  cout << endl;

  cout << "f(x) = e ^ x: " << exp(10) << endl;

  cout << endl;

  cout << "2 ^ 4 " << pow(2, 4) << endl;

  cout << endl;

  cout << "log_10 of 1000: " << log10(1000) << endl;

  cout << endl;

  cout << "Sqrt of 81: " << sqrt(81) << endl;

  return 0;
}